//Declaration:using methods of Math Object
var m=25;
//1> using sqrt() of Math Object
console.log(Math.sqrt(m));//5, will give the square root of that value
console.log("==================");
//2> using min() of Math Object
console.log(Math.min(10,32,45,-78,0));//-78, will give the minimum no from all values
console.log("==================");
//3> using abs() of Math Object
console.log(Math.abs(-15)); //15, will give the absolute value of that particular value
console.log("==================");
//4> using pow() of Math Object
console.log(Math.pow(2,3)); //8, give the power of those values.




