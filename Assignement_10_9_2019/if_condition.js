//1.To check whether Number,String,Object,Undefined is true or false in if condition

if(12)
{
    console.log("from 12");
}

if(-12)
{
    console.log("from -12");
}

if(0.0)
{
    console.log("from 0.0");
}

if("0")
{
    console.log("from \"0\"");
}

if(undefined)
{
    console.log("from undefined");
}
if(null)
{
    console.log("from null");
}
if(0)
{
    console.log("from 0");
}

/*for undefined,null,0.0,0 condition is false
* all other condition it is true*/